package Camera;

public class Photographer {

    private  final  Photographer photographer = new Photographer();

    public  void choice() {
        photographer.choice();
    }
    public void on() {
        photographer.on();
    }
    public void zoom() {
        photographer.zoom();
    }
    public void shutter() {
        photographer.shutter();
    }
    public void select() {
        photographer.select();
    }
    public void photozip() {
        photographer.photozip();
    }
    public void onoff() {
        photographer.onoff();
    }
}
